export function getTodaysTip(lang: Language = "sk"): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000
  );
  const langTips = tips[lang] || tips.sk;
  return langTips[dayOfYear % langTips.length];
}

